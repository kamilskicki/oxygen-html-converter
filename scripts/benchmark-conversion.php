<?php

declare(strict_types=1);

const BENCHMARK_SECTION_COUNT = 500;
const BENCHMARK_ELEMENTS_PER_SECTION = 10;
const BENCHMARK_CSS_BYTES = 1048576;
const BENCHMARK_TIME_LIMIT_SECONDS = 30.0;
const BENCHMARK_MEMORY_LIMIT_BYTES = 536870912;

/**
 * @return array{value: mixed, wallTimeSeconds: float, peakMemoryBytes: int, retainedMemoryBytes: int}
 */
function benchmark_stage(callable $operation): array
{
    gc_collect_cycles();
    $memoryBefore = memory_get_usage(true);
    memory_reset_peak_usage();
    $startedAt = hrtime(true);
    $value = $operation();

    return [
        'value' => $value,
        'wallTimeSeconds' => (hrtime(true) - $startedAt) / 1_000_000_000,
        'peakMemoryBytes' => memory_get_peak_usage(true),
        'retainedMemoryBytes' => memory_get_usage(true) - $memoryBefore,
    ];
}

function benchmark_synthetic_input(): string
{
    $rules = [];
    for ($index = 0; $index < BENCHMARK_SECTION_COUNT; $index++) {
        $rules[] = sprintf(
            '.bench-s-%1$d{padding:%2$dpx;color:#123456;background:#f7f7f7}',
            $index,
            ($index % 8) + 1
        );
    }

    $css = implode("\n", $rules);
    $remainingBytes = BENCHMARK_CSS_BYTES - strlen($css);
    if ($remainingBytes < 4) {
        throw new RuntimeException('Generated CSS rules exceed the configured payload size.');
    }
    $css .= '/*' . str_repeat('x', $remainingBytes - 4) . '*/';

    $sections = [];
    for ($index = 0; $index < BENCHMARK_SECTION_COUNT; $index++) {
        $sections[] = sprintf(
            '<section class="bench-section bench-s-%1$d"><h2>Section %1$d</h2><p>Benchmark copy %1$d</p><div><span>Label</span><strong>Value</strong><a href="#s-%1$d">Read</a></div><ul><li>Alpha</li><li>Beta</li></ul></section>',
            $index
        );
    }

    return '<!doctype html><html><head><meta charset="utf-8"><style>'
        . $css
        . '</style></head><body><main>'
        . implode('', $sections)
        . '</main></body></html>';
}

function benchmark_format_bytes(int $bytes): string
{
    return sprintf('%.2f MiB', $bytes / 1048576);
}

function benchmark_markdown(array $report): string
{
    $lines = [
        '# Conversion Performance Benchmark — 2026-07-10',
        '',
        'Generated by `php scripts/benchmark-conversion.php` using the plugin `TreeBuilder::convert()` entrypoint without WordPress.',
        '',
        '## Workload',
        '',
        sprintf('- Sections: %d', $report['workload']['sections']),
        sprintf('- Approximate HTML elements: %d', $report['workload']['elements']),
        sprintf('- CSS payload: %s (%d bytes)', benchmark_format_bytes($report['workload']['cssBytes']), $report['workload']['cssBytes']),
        sprintf('- Total generated input: %s (%d bytes)', benchmark_format_bytes($report['workload']['inputBytes']), $report['workload']['inputBytes']),
        '',
        '## Environment',
        '',
        sprintf('- PHP: `%s`', $report['environment']['php']),
        sprintf('- OS: `%s`', $report['environment']['os']),
        '',
        '## Stage Measurements',
        '',
        '| Stage | Wall time | Peak memory | Retained memory |',
        '| --- | ---: | ---: | ---: |',
    ];

    foreach ($report['stages'] as $name => $stage) {
        $lines[] = sprintf(
            '| %s | %.3f s | %s | %s |',
            $name,
            $stage['wallTimeSeconds'],
            benchmark_format_bytes($stage['peakMemoryBytes']),
            benchmark_format_bytes($stage['retainedMemoryBytes'])
        );
    }

    $lines = array_merge($lines, [
        '',
        '## Threshold Verdicts',
        '',
        '| Gate | Measured | Threshold | Verdict |',
        '| --- | ---: | ---: | :---: |',
        sprintf(
            '| 1 MiB CSS conversion time | %.3f s | < %.0f s | **%s** |',
            $report['summary']['conversionSeconds'],
            BENCHMARK_TIME_LIMIT_SECONDS,
            $report['verdicts']['time'] ? 'PASS' : 'FAIL'
        ),
        sprintf(
            '| Process peak memory | %s | < %s | **%s** |',
            benchmark_format_bytes($report['summary']['peakMemoryBytes']),
            benchmark_format_bytes(BENCHMARK_MEMORY_LIMIT_BYTES),
            $report['verdicts']['memory'] ? 'PASS' : 'FAIL'
        ),
        sprintf(
            '| Conversion result | success=%s, elements=%d | success and >= %d elements | **%s** |',
            $report['summary']['success'] ? 'true' : 'false',
            $report['summary']['convertedElements'],
            BENCHMARK_SECTION_COUNT * BENCHMARK_ELEMENTS_PER_SECTION,
            $report['verdicts']['result'] ? 'PASS' : 'FAIL'
        ),
        '',
        sprintf('Overall verdict: **%s**.', $report['verdicts']['overall'] ? 'PASS' : 'FAIL'),
        '',
        'Peak memory is the PHP allocator peak observed independently for each stage. Retained memory is the allocator change during that stage and may be negative after garbage collection.',
        '',
    ]);

    return implode(PHP_EOL, $lines);
}

$root = dirname(__DIR__);
$outputPath = $root . '/artifacts/perf/benchmark-2026-07-10.md';
foreach ($argv as $argument) {
    if (preg_match('/^--output=(.+)$/', $argument, $matches) === 1) {
        $outputPath = str_starts_with($matches[1], '/') || preg_match('/^[A-Za-z]:[\\\\\/]/', $matches[1]) === 1
            ? $matches[1]
            : $root . '/' . $matches[1];
    }
}

try {
    $bootstrap = benchmark_stage(static function () use ($root): bool {
        require_once $root . '/tests/bootstrap.php';
        require_once $root . '/src/polyfills.php';
        return true;
    });
    $generation = benchmark_stage('benchmark_synthetic_input');
    $html = $generation['value'];
    $initialization = benchmark_stage(static fn () => new \OxyHtmlConverter\TreeBuilder());
    $builder = $initialization['value'];
    $conversion = benchmark_stage(static fn () => $builder->convert($html));
    $result = $conversion['value'];
    $serialization = benchmark_stage(static function () use ($result): string {
        $json = json_encode($result, JSON_UNESCAPED_SLASHES | JSON_THROW_ON_ERROR);
        return $json;
    });

    $stages = [
        'Bootstrap test runtime' => $bootstrap,
        'Generate synthetic input' => $generation,
        'Initialize converter' => $initialization,
        'Convert through TreeBuilder' => $conversion,
        'Serialize result JSON' => $serialization,
    ];
    foreach ($stages as &$stage) {
        unset($stage['value']);
    }
    unset($stage);

    $peakMemory = max(array_column($stages, 'peakMemoryBytes'));
    $convertedElements = (int) ($result['stats']['elements'] ?? 0);
    $success = (bool) ($result['success'] ?? false);
    $verdicts = [
        'time' => $conversion['wallTimeSeconds'] < BENCHMARK_TIME_LIMIT_SECONDS,
        'memory' => $peakMemory < BENCHMARK_MEMORY_LIMIT_BYTES,
        'result' => $success && $convertedElements >= (BENCHMARK_SECTION_COUNT * BENCHMARK_ELEMENTS_PER_SECTION),
    ];
    $verdicts['overall'] = !in_array(false, $verdicts, true);

    $report = [
        'workload' => [
            'sections' => BENCHMARK_SECTION_COUNT,
            'elements' => BENCHMARK_SECTION_COUNT * BENCHMARK_ELEMENTS_PER_SECTION,
            'cssBytes' => BENCHMARK_CSS_BYTES,
            'inputBytes' => strlen($html),
        ],
        'environment' => [
            'php' => PHP_VERSION,
            'os' => php_uname(),
        ],
        'stages' => $stages,
        'summary' => [
            'conversionSeconds' => $conversion['wallTimeSeconds'],
            'peakMemoryBytes' => $peakMemory,
            'success' => $success,
            'convertedElements' => $convertedElements,
            'serializedBytes' => strlen($serialization['value']),
        ],
        'verdicts' => $verdicts,
    ];

    $directory = dirname($outputPath);
    if (!is_dir($directory) && !mkdir($directory, 0777, true) && !is_dir($directory)) {
        throw new RuntimeException('Failed to create benchmark output directory: ' . $directory);
    }
    if (file_put_contents($outputPath, benchmark_markdown($report)) === false) {
        throw new RuntimeException('Failed to write benchmark report: ' . $outputPath);
    }

    echo json_encode([
        'output' => $outputPath,
        'conversionSeconds' => round($conversion['wallTimeSeconds'], 3),
        'peakMemoryMiB' => round($peakMemory / 1048576, 2),
        'convertedElements' => $convertedElements,
        'verdict' => $verdicts['overall'] ? 'PASS' : 'FAIL',
    ], JSON_PRETTY_PRINT | JSON_UNESCAPED_SLASHES) . PHP_EOL;
    exit($verdicts['overall'] ? 0 : 1);
} catch (Throwable $exception) {
    fwrite(STDERR, $exception->getMessage() . PHP_EOL);
    exit(1);
}
